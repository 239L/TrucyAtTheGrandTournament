(function() {
    "use strict"

    Game_Actor.prototype.levelUpByItem = function(){
      this.changeLevel(this.level + 1, false);
    };

    Game_Actor.prototype.levelDownByItem = function(){
        this.changeLevel(this.level - 1, false);
      };

    Game_Action.prototype.canUseLevelUpItem = function(target) {
        return this.item().meta["LvUpItem"] && target.actor && !target.isMaxLevel();
    };

    Game_Action.prototype.canUseLevelDownItem = function(target) {
        return this.item().meta["LvDownItem"] && target.actor && !target.isMaxLevel() || target.isMaxLevel();
    };

    var GA_hasItemAnyValidEffects = Game_Action.prototype.hasItemAnyValidEffects;
        Game_Action.prototype.hasItemAnyValidEffects = function(target) {
            if (this.canUseLevelUpItem(target) || this.canUseLevelDownItem(target)) return true;
            return GA_hasItemAnyValidEffects.apply(this, arguments);
        };

    var GA_applyItemUserEffect = Game_Action.prototype.applyItemUserEffect;
        Game_Action.prototype.applyItemUserEffect = function(target) {
            if (this.canUseLevelUpItem(target)) { 
                target.levelUpByItem();
            } else if (this.canUseLevelDownItem(target)) {
                target.levelDownByItem();
            };
        GA_applyItemUserEffect.apply(this, arguments);
    };

})();