/** /*:
* 
* @author ShadowDragon
* @plugindesc Base plugin required for my Plugins.
*
* @help
*
* Flip the Screen X, Y or both XY using scriptcall
*
* flipX(); = flip screen horizontally
* flipY(); = flip screen vertically
* flipXY(); = flipt both screen horizontally and vertically
*
* flipMsgX(); = flip MessageBox horizontally
* flipMsgY(); = flip MessageBox vertically
* flipMsgXY(); = flipt both MessageBox horizontally and vertically
*
* flipScreenX(); = flip Screen and MessageBox horizontally
* flipScreenY(); = flip Screen and MessageBox vertically
* flipScreenXY(); = flipt both Screen and MessageBox horizontally and vertically
*
* 
* to reverse it back to its original position:
* 
* reverseX(); flip screen back horizontally
* reverseY(); flip screen back vertically
* reverseXY(); flip screen back horizontally and vertically
*
* reverseMsgX(); = flip MessageBox back horizontally
* reverseMsgY(); = flip MessageBox back vertically
* reverseMsgXY(); = flipt both back MessageBox horizontally and vertically
*
* reverseScreenX(); = flip Screen and MessageBox back horizontally
* reverseScreenY(); = flip Screen and MessageBox back vertically
* reverseScreenXY(); = flipt both Screen and MessageBox back horizontally and vertically
*
*/

