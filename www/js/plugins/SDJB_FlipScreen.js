// Flip Screen X, Y or BOTH
    const flipX = function() {
        SceneManager._scene._spriteset._tilemap.scale.x = -1;
        SceneManager._scene._spriteset._tilemap.position.x += Graphics._width;
    };

    const flipY = function() {
        SceneManager._scene._spriteset._tilemap.scale.y = -1;
        SceneManager._scene._spriteset._tilemap.position.y += Graphics._height;
    };

    const flipXY = function() {
        SceneManager._scene._spriteset._tilemap.scale.x = -1;
        SceneManager._scene._spriteset._tilemap.scale.y = -1;
        SceneManager._scene._spriteset._tilemap.position.x += Graphics._width;
        SceneManager._scene._spriteset._tilemap.position.y += Graphics._height;
    };

    const reverseX = function() {
        SceneManager._scene._spriteset._tilemap.scale.x = 1;
        SceneManager._scene._spriteset._tilemap.position.x -= Graphics._width;
    };
    
    const reverseY = function() {
        SceneManager._scene._spriteset._tilemap.scale.y = 1;
        SceneManager._scene._spriteset._tilemap.position.y -= Graphics._height;
    };

    const reverseXY = function() {
        SceneManager._scene._spriteset._tilemap.scale.x = 1
        SceneManager._scene._spriteset._tilemap.scale.y = 1;
        SceneManager._scene._spriteset._tilemap.position.x -= Graphics._width;
        SceneManager._scene._spriteset._tilemap.position.y -= Graphics._height;
    };

